    # –£–¥–∞–ª—è–µ–º –ø—Ä–∞–≤–∏–ª–æ –¥–ª—è —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–æ–≥–æ –ø–æ—Ä—Ç–∞ 22/tcp, –ï–°–õ–ò –Ω–æ–≤—ã–π –ø–æ—Ä—Ç –ù–ï 22
